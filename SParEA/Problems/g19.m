function [f,g] = g19_true(x)
b = [-40,-2,-0.25,-4,-4,-1,-40,-60,5,1];
e = [-15,-27,-36,-18,-12];
c = [30,-20,-10,32,-10;...
    -20,39,-6,-31,32;...
    -10,-6,10,-6,-10;...
    32,-31,-6,39,-20;...
    -10,32,-10,-20,30];
d = [4,8,10,6,2];
a = [-16,2,0,1,0;...
    0,-2,0,0.4,2;...
    -3.5,0,2,0,0;...
    0,-2,0,-4,-1;...
    0,-9,-2,1,-2.8;...
    2,0,-4,0,0;...
    -1,-1,-1,-1,-1;...
    -1,-2,-3,-2,-1;...
    1,2,3,4,5;...
    1,1,1,1,1];

g(:,1) = -2.*(sum((c(:,1)'.*x(:,11:15)),2)) - 3.*d(1)*x(:,11).^2 - e(1) + sum((a(:,1)'.*x(:,1:10)),2);
g(:,2) = -2.*(sum((c(:,2)'.*x(:,11:15)),2)) - 3.*d(2)*x(:,12).^2 - e(2) + sum((a(:,2)'.*x(:,1:10)),2);
g(:,3) = -2.*(sum((c(:,3)'.*x(:,11:15)),2)) - 3.*d(3)*x(:,13).^2 - e(3) + sum((a(:,3)'.*x(:,1:10)),2);
g(:,4) = -2.*(sum((c(:,4)'.*x(:,11:15)),2)) - 3.*d(4)*x(:,14).^2 - e(4) + sum((a(:,4)'.*x(:,1:10)),2);
g(:,5) = -2.*(sum((c(:,5)'.*x(:,11:15)),2)) - 3.*d(5)*x(:,15).^2 - e(5) + sum((a(:,5)'.*x(:,1:10)),2);

temp = zeros(size(x,1),1);
for j=1:size(c,2)
    temp = temp + sum((c(:,j)'.*x(:,11:15)),2).*x(:,10+j);
end

f(:,1) = temp + 2.*sum((d.*x(:,11:15).^3),2) - sum((b.*x(:,1:10)),2);

return
